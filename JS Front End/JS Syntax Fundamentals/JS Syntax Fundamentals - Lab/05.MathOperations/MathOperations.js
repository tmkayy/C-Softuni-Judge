function math (num1, num2, s){
  switch(s){
    case '+':
      console.log(num1+num2);
      break;
    case '-':
      console.log(num1-num2);
      break;
    case '*':
      console.log(num1*num2);
      break;
    case '/':
      console.log(num1/num2);
      break;
    case '%':
      console.log(num1%num2);
      break;
    case '**':
      console.log(num1**num2);
      break;
  }
}